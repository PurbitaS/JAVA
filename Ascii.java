public class Ascii{
	public static void main(String[] args)
	{
		char c1= 'p';
		char c2= 'u';
		char c3= 'r';
		int ascii1= c1, ascii2= c2, ascii3= c3;
		System.out.println("The ASCII value for the first character 'p' is: " +ascii1);
		System.out.println("The ASCII value for the second character 'u' is: " +ascii2);
		System.out.println("The ASCII value for the third character 'r' is: " +ascii3);
	}
}